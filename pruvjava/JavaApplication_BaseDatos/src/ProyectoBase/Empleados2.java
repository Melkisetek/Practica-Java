    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean OK = true;
        String mensaje = "";
        String nombre =txtNombre.getText();
        String DPI = txtDPI.getText();
        String fechaNacimiento = txtNacimiento.getText();
        String telefono = txtTelefono.getText();
        String direccion = txtDireccion.getText();
        
        if(nombre.length()==0){
           OK = false;
           mensaje+="Escriba el nombre, por favor\n";
       }
        
        if(DPI.length()==0){
           OK=false;
           mensaje+="Escriba DPI, por favor\n";
       }
        
        if(fechaNacimiento.length()==0){
            OK = false;
            mensaje+="Escriba fecha de nacimiento, por favor\n";
        }
        
        if(telefono.length()==0){
            OK = false;
            mensaje+="Escriba número de telefono, por favor\n";
        }
        
        if(direccion.length()==0){
            OK = false;
            mensaje+="Escriba una dirección, por favor\n";
        }

            
        
        if(OK){
            controlDatos1 control = new controlDatos1();
            control.insertar(nombre, DPI, fechaNacimiento, telefono, direccion);
            
            txtNombre.setText("");
            txtDPI.setText("");
            txtNacimiento.setText("");
            txtTelefono.setText("");
            txtDireccion.setText("");
            
        }
        else JOptionPane.showMessageDialog(null, mensaje);
        
//        Datatxt escribirTxt=new Datatxt ();
//        escribirTxt.guardarArchivo(emp);
        
    }//GEN-LAST:event_btnGuardarActionPerformed

